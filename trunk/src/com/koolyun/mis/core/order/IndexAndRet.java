package com.koolyun.mis.core.order;

public class IndexAndRet {
	int index;
	int ret;

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}

	public int getRet() {
		return ret;
	}

	public void setRet(int ret) {
		this.ret = ret;
	}

	public IndexAndRet(int index, int ret) {
		super();
		this.index = index;
		this.ret = ret;
	}

}
